/*
Implement a countdown timer that counts down from 81 to 65.
 */

//using for loop
console.log("Using For Loop");
for (let x = 81; x >= 65; x--) {
  console.log("count down: ", x);
}

//using while loop
console.log("using while loop");
let i = 21;
while (i >= 15) {
  console.log("count down: ", i);
  i--;
}
