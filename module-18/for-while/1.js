/*
"I will invest at least 6 hrs every single day for next 60 days!" this message 60 times. So display this.
 */
for (let x = 1; x <= 60; x++) {
  console.log(
    "I will invest at least 6 hrs every single day for next 60 days!",
    x
  );
}

//using while loop
console.log("using while loop");
let x = 1;
while (x <= 60) {
  console.log(
    "I will invest at least 6 hrs every single day for next 60 days!",
    x
  );
  x++;
}
